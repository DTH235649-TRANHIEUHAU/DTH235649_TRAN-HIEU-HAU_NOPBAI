'''
Những kiểu dữ liệu cơ bản trong Python
1.Số (Numbers):
    int: số nguyên
    float: số thực
    complex: số phức
2.Chuỗi (Strings):
    - Hỗ trợ lưu văn bản
    - Hỗ trợ cắt chuỗi, nối chuỗi, duyệt ký tự
3.Boolean (Bool):
    - Chỉ có hai giá trị True và False
    - Thường dùng trong các biểu thức điều kiện, so sánh
4.Danh sách (List):
    - Dùng để lưu trữ nhiều giá trị trong một biến
    - Hỗ trợ các phép toán như thêm, xóa, sửa, duyệt phần tử
5.Tupla (Tuple):
    - Giống danh sách nhưng không thể thay đổi (immutable)
    - Tạo ra để bảo vệ dữ liệu không bị thay đổi   
6.Từ điển (Dictionary):
    - Lưu trữ dữ liệu theo cặp key-value
    - Truy cập nhanh theo key
'''